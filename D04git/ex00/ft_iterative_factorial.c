int		ft_iterative_factorial(int nb)
{
	int r;

	r = 1;
	if (nb > 12 || nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (nb)
	{
		r = r * nb--;
	}
	return (r);
}
